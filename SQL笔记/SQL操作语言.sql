关联操作
字段默认值
	default 值
	--当该字段没有被赋值的时候，该字段就是这个值！
主键唯一索引
	主键(PK,primary Key)  默认的就是 not null.是不能为空的。可以为0或者是负数。
		|--可以唯一标识某条记录的字段，或是字段的集合。就是主键。它可以是真实实体的属性。
		 --但是，常见的解决方案，还是利用一个与实体信息不相关的属性。作为唯一标识符。
		|--可以在某个字段后面添加该关键来声明是主键。或者在创建表格的最后一行：primary key(指定字段) 。来指定键。
		|--可以组合主键。一个主键内包含多个字段。而不是多个字段都是主键。
		|--可以手动插入主键的值，但是不能重复。
		|--可以更新主键的值，同样。也是不能重复。
自动增长
	auto_incremen
	每次插入记录的时。主键的值，自动增加1.对数据类型有约束。必须是整形。建立在索引之上。
	设置了自动增长好。可以插入 null 或者是不插入。
	初始值可以是设置。默认是1.通过修改表选项。修改表选项的时候插入的值。
	
是否允许为空
	not null
	--规定一个字段的值是否可以是null。一般情况下。默认的是为空的！
