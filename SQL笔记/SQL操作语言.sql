	校对规则-------
	每一套编码字符集都有与之相关的校对规则。
	指的就是在当前编码下。字符之间的比较顺序。这个就是所谓的校对规则。
在设置字符集时，可以试着当前字符集所采用的校对规则。如果不设置校对规则。
那么每一个字符集，都有一个默认的校对规则。
以GBK为例！
	影响数据的只是字符集。而影响数据的排序的就是字符集的校对规则。
