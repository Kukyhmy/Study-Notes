	3，数据库管理语言(针对数据库软件服务进行操作)DCL
规则：
	数据库命名规则
		标识符(数据库名)命名规则-大小写取决于当前操作系统。
		见名知意，命名要有意义。推荐使用下划线方法。
		原则上是可以使用任意字符。数字，符号，甚至是中文。
		但是一些特殊的组合，例如纯数字，特殊符号，包括mysql的内部关键字。应该使用标识符限定符(``)来包裹。	
		中文可以，但是要求客户端编码。
	数据表的命名
		同上的规则，且。为了区分相同逻辑表名的不同应用。应该加上表前缀。
		可以李荣跨数据库重命名表，可以为数据库重命名。创建一个新的数据库，旧数据库内的表。都rename到新的数据库内。删除旧的数据库。
